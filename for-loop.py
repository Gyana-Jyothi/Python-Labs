print("Count to 10!")
# The `for` loop is used to iterate over a sequence of numbers.
# In this case, it will iterate over the numbers from 0 to 10 (inclusive).
for x in range(0, 11):
    # The indentation indicates that the following block of code belongs to the loop.
    # It will be executed for each value of `x` in the specified range.

    # The `print` function is used to display the current value of `x`.
    print(x)

# The loop ends here, and any code following the loop that is not indented
# will be executed after the loop has completed.

